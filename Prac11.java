public class Prac11 {
    public static void main(String[] args) {
        double radius = 7.5;
        double pi = 3.14159265359;
        double perimeter = pi * 2 * radius;
        double area = pi * radius * radius;
        System.out.println("Perimeter is " + perimeter);
        System.out.println("Area is " + area);
    }
}
